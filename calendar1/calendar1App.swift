    // - App プロトコル要件。
    // - 返り値は Scene 型で、実際に画面に表示されるウィンドウやシーンを定義します。
    // - WindowGroup は iOS アプリの標準的なウィンドウ管理を行います（複数ウィンドウ対応）。
    var body: some Scene {
