        // WindowGroup
        // ========================================
        // - iOS アプリの単一ウィンドウを管理するシーン。
        // - 内部に ContentView を配置することで、最初の表示内容を定義します。
        // - `some Scene` はコンパイラが型推論するため、具体的な型は指定しません。
